import UIKit


extension UIImage {
    
    enum MyCustomImages {
        static let PersonalInfo     = UIImage(named: Constants.Images.personalInfo)!
        static let CareerObjective  = UIImage(named: Constants.Images.careerObjective)!
        static let WorkSummary      = UIImage(named: Constants.Images.workSummary)!
        static let Skills           = UIImage(named: Constants.Images.skills)!
        static let EducationDetails = UIImage(named: Constants.Images.educationDetails)!
        static let ProjectDetails   = UIImage(named: Constants.Images.projectDetails)!
        static let Trash            = UIImage(named: Constants.Images.trash)!
        static let EditImage        = UIImage(named: Constants.Images.editImage)!
        
        static let DefaultAvatar    = UIImage(named: Constants.Images.defaultAvatar)!
    }
    
}
